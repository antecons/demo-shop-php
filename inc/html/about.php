            <p class="lead">This demo webshop is created to show how the <a href="//www.antecons.net">Antecons</a> recommendation engine can be integrated to a custom webshop using the <a href="//api.antecons.net">Antecons API</a>.</p>
            <h2>Shop features</h2>
            <p>This webshop is not a fully functional e-commerce platform but there are a few standard webshop capabilities implemented such as viewing products and placing orders.</p>
            <p>Feel free to place some orders. No address or registration is required.</p>
            <h2>Antecons recommendations</h2>
            <p>The Antecons recommendations are shown on the different product pages throughout the shop and they are specific for the product that is shown.</p>
            <p>Since there are only a few products on this shop, it is very likely that the same products will show up on multiple product pages.</p>
            <p>Whenever an order is placed, it is sent to Antecons and is immediately part of the analysis that creates the recommendations on this shop.</p>
